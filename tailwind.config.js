const focusVisible = () => ({ addVariant, e }) => {
  addVariant('focus-visible', ({ modifySelectors, separator }) => {
    modifySelectors(({ className }) => {
      return `.${e(
        `focus-visible${separator}${className}`
      )}.focus-visible, .${e(
        `focus-visible${separator}${className}`
      )}:focus-visible`
    })
  })
}

